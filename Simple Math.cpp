#include <iostream>
using namespace std;

int main() {
    cout << "3 + 4 ="   << 3+4 << endl;
    cout << "5 - 6 ="   << 5-6 << endl;
    cout << "10 * 6 ="  << 10*6 <<endl;
    cout << "5 / 2 ="   << 5/2 <<endl;
    cout << "5.0 / 2.0 =" << 5.0/2.0 <<endl;
    cout << "6 % 4 ="   << 6%4 <<endl;

    return 0;

}
